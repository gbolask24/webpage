const btn =
  "inline-flex size-10 items-center justify-center rounded-xl border border-white/10 bg-white/[0.03] text-zinc-400 transition-all hover:border-white/20 hover:bg-white/[0.06] hover:text-white";

export function ShareButtons({ title, url }: { title: string; url: string }) {
  const [copied, setCopied] = useState(false);
  const [canNativeShare, setCanNativeShare] = useState(false);

  useEffect(() => {
    setCanNativeShare(typeof navigator !== "undefined" && !!navigator.share);
  }, []);

  const t = encodeURIComponent(title);
  const u = encodeURIComponent(url);
  const targets = [
    { name: "X", href: `https://x.com/intent/tweet?text=${t}&url=${u}&via=GbolagadeHQ`, icon: XIcon },
    { name: "LinkedIn", href: `https://www.linkedin.com/sharing/share-offsite/?url=${u}`, icon: LinkedInIcon },
    { name: "Facebook", href: `https://www.facebook.com/sharer/sharer.php?u=${u}`, icon: FacebookIcon },
    { name: "Reddit", href: `https://www.reddit.com/submit?url=${u}&title=${t}`, icon: RedditIcon },
    { name: "WhatsApp", href: `https://wa.me/?text=${encodeURIComponent(`${title} ${url}`)}`, icon: WhatsAppIcon },
    { name: "Email", href: `mailto:?subject=${t}&body=${u}`, icon: EmailIcon },
  ];

  async function handleCopy() {
    try {
      await navigator.clipboard.writeText(url);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch {
      // clipboard unavailable; leave button in default state
    }
  }

  async function handleNativeShare() {
    try {
      await navigator.share({ title, url });
    } catch {
      // user cancelled or share failed; no-op
    }
  }

  return (
    <div className="flex flex-wrap items-center gap-2">
      <span className="mr-1 text-xs uppercase tracking-widest text-zinc-500">
        Share
      </span>
      {targets.map((target) => (
        <a
          key={target.name}
          href={target.href}
          target="_blank"
          rel="noopener noreferrer"
          aria-label={`Share on ${target.name}`}
          title={`Share on ${target.name}`}
          className={btn}
        >
          {target.icon}
        </a>
      ))}
      <button
        type="button"
        onClick={handleCopy}
        aria-label="Copy link"
        title={copied ? "Copied!" : "Copy link"}
        className={btn}
      >
        {copied ? CheckIcon : LinkIcon}
      </button>
      {canNativeShare && (
        <button
          type="button"
          onClick={handleNativeShare}
          aria-label="Share"
          title="Share"
          className={btn}
        >
          {ShareIcon}
        </button>
      )}
    </div>
  );
}
